namespace TMP.Extensions
{
    using System;

    /// <summary>
    /// DateSpan class used to computer difference of two dates
    /// </summary>
    public class DateSpan
    {
        /// <summary>
        /// Difference in days
        /// </summary>
        public int Days
        {
            get;
            set;
        }

        /// <summary>
        /// Difference in months
        /// </summary>
        public int Months
        {
            get;
            set;
        }

        /// <summary>
        /// Difference in years
        /// </summary>
        public int Years
        {
            get;
            set;
        }
    }
}
